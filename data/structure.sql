CREATE TABLE users (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    username TEXT NOT NULL UNIQUE,
);

CREATE TABLE auth (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    password TEXT NOT NULL
    user_id INTEGER NOT NULL,
    FOREIGN KEY(user_id) REFERENCES users(id)  
);

CREATE TABLE characters (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    level INTEGER NOT NULL,
    description TEXT,
    user_id INTEGER NOT NULL,
    FOREIGN KEY(user_id) REFERENCES users(id)
);
